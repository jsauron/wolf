#include "libft.h"

int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (i <= nb)
	{
		if (i > 46340)
			return (0);
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
