#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	temp;

	i = 0;
	temp = 0;
	if (c == '\0')
		return ((char *)s + ft_strlen(s));
	while (s[i])
	{
		if (s[i] == c)
			temp = i + 1;
		i++;
	}
	return ((temp != 0) ? (char *)s + temp - 1 : 0);
}
